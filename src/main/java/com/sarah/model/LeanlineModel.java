package com.sarah.model;

public class LeanlineModel {
		private Integer id;

	    private String downloadurl;

	    private String name;

	    private String leanlineCategory;

		/**
		 * @return the id
		 */
		public Integer getId() {
			return id;
		}

		/**
		 * @param id the id to set
		 */
		public void setId(Integer id) {
			this.id = id;
		}

		/**
		 * @return the downloadurl
		 */
		public String getDownloadurl() {
			return downloadurl;
		}

		/**
		 * @param downloadurl the downloadurl to set
		 */
		public void setDownloadurl(String downloadurl) {
			this.downloadurl = downloadurl;
		}

		/**
		 * @return the name
		 */
		public String getName() {
			return name;
		}

		/**
		 * @param name the name to set
		 */
		public void setName(String name) {
			this.name = name;
		}

		/**
		 * @return the leanlineCategory
		 */
		public String getLeanlineCategory() {
			return leanlineCategory;
		}

		/**
		 * @param leanlineCategory the leanlineCategory to set
		 */
		public void setLeanlineCategory(String leanlineCategory) {
			this.leanlineCategory = leanlineCategory;
		}

}
